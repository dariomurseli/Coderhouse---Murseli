class Paciente {
    constructor(
      nombre,
      apellido,
      telefono,
      edad,
      email,
      obraSocial,
      diaAsignado,
      horaAsignada
    ) {
      this.nombre = nombre;
      this.apellido = apellido;
      this.telefono = telefono;
      this.edad = edad;
      this.email = email;
      this.obraSocial = obraSocial;
      this.diaAsignado = diaAsignado;
      this.horaAsignada = horaAsignada;
    }
  }